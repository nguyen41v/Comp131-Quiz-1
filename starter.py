def common_prefix(word1, word2):
    """Find the common prefix of two words.

    Arguments:
        word1 (str): The first word.
        word2 (str): The second word.

    Returns:
        str: The common characters at the start of the two words, or the empty
            string if the words do not share a common prefix.

    Examples:

        >>> print(common_prefix("delicatessen", "delicious"))
        delic

        >>> print(common_prefix("identical", "identical"))
        identical

        In the following example, the two words have no letters in common, so
        the function returns the empty string. I print it between arrows to show
        that a result is returned, but is empty.

        >>> result = common_prefix("similar", "dissimilar")
        >>> print("-->" + result + "<--")
        --><--

        XXX

        >>> result = common_prefix("", "")
        >>> print("-->" + result + "<--")
        --><--

        >>> result = common_prefix("", "irrelevant")
        >>> print("-->" + result + "<--")
        --><--

        >>> result = common_prefix("irrelevant", "")
        >>> print("-->" + result + "<--")
        --><--

    """
    prefix = ""
    word1 = word1.lower()
    word2 = word2.lower()
    if (len(word1) > 0) and (len(word2) > 0):
        if len(word1) >= len(word2):
            size = len(word2)
        else:
            size = len(word1)
        for i in range(size):
            if word1[i] == word2[i]:
                prefix += word1[i]
            else:
                break
    return prefix

