def can_enroll(num_enrolled, num_seats, num_reserved_remaining, has_reservation):
    """Determine if a student can enroll in a course at Oxy.

    Oxy's course enrollment system allows professors to reserve seats in a
    course for specific students. This function models how the system determines
    whether a student can enroll. See the examples below for how this system
    works.

    Arguments:
        num_enrolled (int): The number of students already enrolled.
            This argument is always non-negative.
        num_seats (int): The total number of seats in the course.
            This argument is always positive.
        num_reserved_remaining (int): The number of reserved seats remaining.
            This argument is always non-negative.
        has_reservation (bool): Whether the student has a reservation.

    Returns:
        bool: True if the student can enroll in the course, False otherwise.

    Examples:

        A student without a reservation can enroll if the course is not full and
        there is an unreserved seat.

        >>> print(can_enroll(0, 10, 0, False))
        True

        >>> print(can_enroll(10, 10, 0, False))
        False

        >>> print(can_enroll(7, 10, 2, False))
        True

        In the following example, the student CANNOT enroll because the
        remaining seats are all reserved.

        >>> print(can_enroll(8, 10, 2, False))
        False

        Having a reservation would let them enroll:

        >>> print(can_enroll(8, 10, 2, True))
        True

        >>> print(can_enroll(8, 10, 3, True))
        True

        However, the reservation doesn't help if the course is already full:

        >>> print(can_enroll(10, 10, 3, True))
        False

        >>> print(can_enroll(11, 10, 3, True))
        False

    """
    if num_enrolled < num_seats:
        if (num_enrolled + num_reserved_remaining) < num_seats:
            return True
        elif has_reservation:
                return True
    return False


